<p>9. Инт. Офис. День.<br/>
Следующий кадр.<br/>
Герой со знакомым в офисе ЧОПа.<br/>
Знакомый (озабоченно): Паш, тут такое дело. У нас стволы не совсем чисто оформлены…, (с усмешкой): …ну, сам знаешь, как всё это сейчас делается. Денежку кому-то дали, чтоб побыстрей! Разрешения, вроде, выдали. А пёс их знает, как они уж там реально «оформили»!.. Может, левота всё. А щас проверка МВД-эшная всех ЧОПов идёт. Сплошная. Кампания очередная. Показуха. Но лицензию отнять могут. Короче, лучше нам щас эти стволы не светить. От греха! Вывезти их куда-нибудь пока. В безопасное место. На недельку буквально. А успокоится всё, проверка эта пройдёт, и опять всё на круги своя вернётся и забудется. Как обычно. Спокойненько их назад тогда и привезём. Я бы сам всё сделал, да я в командировку улетаю через два часа, у меня уже билет на самолёт на руках, ты же знаешь. Займись этим, а?<br/>
Герой, смотрит, колеблясь, он чувствует интуитивно какую-то опасность, но не решается отказать человеку, которому он обязан работой.<br/>
Герой (после паузы, нехотя): Ладно.<br/>
Знакомый (роясь в столе, просматривая какие-то бумаги, и откладывая некоторые себе в кейс): Только дома не храни. Ну на фиг! Хоть они и официальные почти в общем-то, но… Знаешь, бережёного, как говорится, бог бережёт!<br/>
Герой (задумчиво): А небережёного конвой стережёт…<br/>
Знакомый (замерев на секунду и коротко, с усмешкой, глянув на героя): Во-во!<br/>
(Опять начиная рыться, не поднимая глаз): У тебя гараж есть?<br/>
Герой: Да есть, там, от отца остался. Развалюха. Название одно.<br/>
Знакомый: А где?<br/>
Герой: Да во дворе у нас прямо. Под окнами.<br/>
Знакомый: Ну вот, там и спрячь. Он на сигнализации?<br/>
Герой (удивлённо): Ты чё, Гриш!? Какая сигнализация! Чего охранять-то? Мою помойку, что ль? Кому она нужна?<br/>
Знакомый: Ну, неважно! Нет, так нет. На неделю же всего… (Захлопывает кейс.) Всё, Паш, пока! Опаздываю уже! Значит, договорились? Подержишь в гараже недельку?.. Вот тебе ключ от сейфа. (Даёт герою ключ.) Вечером сегодня же заберёшь все стволы и увезёшь. Лады? (Протягивает герою руку, прощаясь.) Говорить, естественно, никому ничего не надо. Всё строго между нами.<br/>
Герой (пожимая руку): Ладно. Будет сделано, гражданин начальник.<br/>
Знакомый: Пока, Паш!<br/>
Герой: Пока. Счастливо долететь.<br/>
Знакомый: К чёрту!<br/>
Герой (смеясь): Нет, к чёрту не надо!<br/>
Знакомый (тоже смеясь): Ладно! Всё, пока. Я на тебя надеюсь.<br/>
Уходит.</p>
