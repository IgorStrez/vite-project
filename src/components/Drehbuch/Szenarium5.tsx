<p>Герой, молодой парень, потомственный офицер. После событий 93-ого года уходит из армии и становится тайным суперкиллером одной из крупнейших московских группировок. Одним из его клиентов оказывается вор в законе, после чего герой оказывается в безвыходном положении. Его ищут все: и менты, и братва. Он решает убить всех членов группировки, знающих про него, и выйти из игры. В принципе это ему почти удаётся, но герой − живой человек, и, как и у любого человека, у него есть свои слабые места…</p>
